#include <cstdio>
#include <vector>

int main(void) {
    int cases;
    scanf("%d\n", &cases);

    while(cases > 0) {
        int fireStations, intersections;
        scanf("%d %d", &fireStations, &intersections);

        for(int i=0; i<fireStations; i++) {
            int fsNode;
            scanf("%d", &fsNode);
        }

        for(int i=0; i<intersections; i++) {
            int a,b,len;
            scanf("%d %d %d", &a, &b, &len);
        }

        cases--;
    }

    return 0;
}
