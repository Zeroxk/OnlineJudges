#include <cstdio>
#include <vector>

int main(void) {

    int cases;
    scanf("%d",&cases);
    while(cases > 0) {
    
        int cities,roads;
        scanf("%d %d", &cities, &roads);

        for(int r=0; r<roads; r++) {
        
            int city1,city2,passengers;
            scanf("%d %d %d",&city1,&city2,&passengers);

        }

        cases--;
    }

    return 0;
}
